function calcSeed(A,times) {
    var seed = 1;
    for (var i=0; i<A.length; ++i) {
        seed=(seed*Math.abs(A[i])+1) % 2147483647;
    }
    for (var i=0; i<times; ++i) {
        seed = seed * 16807 % 2147483647;
    }
    return seed;
}

var gBattle = undefined;
function setupCost (setup,hero,prom) {
    if (prom === undefined) prom = Array(hero.length).fill(0);
    var cost=0;
    for (var i=0; i<setup.length; ++i) {
        if (setup[i]>=0) cost+=MONSTERS[setup[i]].cost;
        else if (setup[i]<=-2 && setup[i]>-(2+HERO.length)) { 
            var hid = -(setup[i]+2);
            var stats=level2stats(hid,hero[hid],prom[hid]);
            var val = stats.hp * stats.atk;
            cost += Math.round(val*Math.sqrt(val)-1000)
        }
    }
    return cost;
}
function beginBattle (date,namea,nameb,A,B,back,heroA,heroB,tinfo,next,promA,promB) {
    if (promA===undefined) promA = Array(HERO.length).fill(0);
    if (promB===undefined) promB = Array(HERO.length).fill(0);
    gBattle = {
        back: back, // back button
        date: date, // date when battle happen
        left: namea, // left player name
        right: nameb, // right player name
        begin: Date.now(), // battle begin
        step: 0, // current step
        dstep: -1, // step executed
        steps: [], // list of steps
        you: [undefined,undefined,undefined,undefined,undefined,undefined,undefined], // your status
        other: [undefined,undefined,undefined,undefined,undefined,undefined,undefined], // other status
        turn: -1,
        heroa:heroA,
        herob:heroB,
        proma:promA,
        promb:promB,
        costa:setupCost(A,heroA,promA),
        costb:setupCost(B,heroB,promB),
        tinfo: tinfo,
        next: next,
        isBossBattle: false,
        bossDmg: 0
    }
    var left = {}, right = {};
    seedA = createUnits(A,heroA,promA,left,calcSeed(B,100));
    seedB = createUnits(B,heroB,promB,right,calcSeed(A,100));
    spawnUnits(left,right);
    leftTurn = calcTurn0(left,right,seedA,0);
    rightTurn = calcTurn0(right,left,seedB,1);
    if (rightTurn) nextRightTurn=doTurn(right,left,rightTurn,leftTurn,1);
    if (leftTurn) nextLeftTurn=doTurn(left,right,leftTurn,rightTurn,0);

    if (right.length>0) {
        gBattle.steps.push({
            action:"FIGHT",
            target:"other",
        });
    }
    if (left.length>0) {
        gBattle.steps.push({
            action:"FIGHT",
            target:"you",
        });
    }
    killUnits(right,1);
    killUnits(left,0);
    var turns = 100;
    while (isAlive(left) && isAlive(right) && --turns) {
        leftTurn = [calcTurn(left,right,calcSeed(B,turns))];
        rightTurn = [calcTurn(right,left,calcSeed(A,turns))];
        var subturns=10;
        while ((leftTurn.length || rightTurn.length) && --subturns) {
            if (leftTurn.length==0) leftTurn=[getTurnData(left.setup.length,right.setup.length)];
            if (rightTurn.length==0) rightTurn=[getTurnData(right.setup.length,left.setup.length)];
            if (rightTurn) responseRight=doTurn(right,left,rightTurn[0],leftTurn[0],1);
            if (leftTurn) responseLeft=doTurn(left,right,leftTurn[0],rightTurn[0],0);    
            leftTurn.shift();
            rightTurn.shift();
            killUnits(right,1);
            killUnits(left,0);
            if (responseLeft.self!==undefined) leftTurn.push(responseLeft.self);
            if (responseLeft.other!==undefined) rightTurn.push(responseLeft.other);
            if (responseRight.self!==undefined) rightTurn.push(responseRight.self);
            if (responseRight.other!==undefined) leftTurn.push(responseRight.other);
        }
        endTurn(left);
        endTurn(right);
    }

    var res = {
        result: undefined,
        dmga: left.dmg,
        dmgb: right.dmg,
        turns: 100-turns,
    };
    if (left.setup.length==0 && right.setup.length>0) {
        res.result = -1;
        gBattle.steps.push({
            action:"RESULT",
            target:"loss",
        });
    } else if (right.setup.length==0 && left.setup.length>0) {
        res.result = 1;
        gBattle.steps.push({
            action:"RESULT",
            target:"win",
        });
    } else {
        res.result = 0;
        gBattle.steps.push({
            action:"RESULT",
            target:"draw",
        });
    }
    return res;
}

