function objCopy(obj) {
    var ret = obj;
    if (typeof obj === "object") {
        if (Array.isArray(obj)) {
            ret = [];
            for (var i=0; i<obj.length; ++i) {
                ret[i] = objCopy(obj[i]);
            }
        } else {
            ret = {};
            for (var key in obj) {
                ret[key] = objCopy(obj[key]);
            }
        }
    }
    return ret;
}

function spawnUnits(left,right) {
    for (var i=Math.max(left.setup.length,right.setup.length)-1; i>=0; --i) {
        if (i<right.setup.length) {
            gBattle.steps.push({
                action:"SPAWN",
                target:"other",
                pos:i+1,
                data:objCopy(right.setup[i])
            });
        }
        if (i<left.setup.length) {
            gBattle.steps.push({
                action:"SPAWN",
                target:"you",
                pos:i+1,
                data:objCopy(left.setup[i])
            });
        }
    }
    if (right.setup.length>0) {
        gBattle.steps.push({
            action:"FIGHT",
            target:"other",
        });
    }
    if (left.setup.length>0) {
        gBattle.steps.push({
            action:"FIGHT",
            target:"you",
        });
    }
}

function WBatk (lvl) {
    return 1;
    var dmg=0;
    var d=0;
    for (var i=0; i<lvl; ++i) {
        ++d;
        if (d>=dmg) {
            d=0;
            dmg++;
        }
    }
    return dmg;
}

function createUnits (userData,hero,prom,battleData,seed) {
    battleData.setup=[];
    var easter = false;
    for (var i=userData.length-1; i>=0; --i) {
        var hpos = -2*1-userData[i];
        if ([97,98,99,161,162,164].indexOf(hpos)!==-1) easter=true;
    }
    for (var i=userData.length-1; i>=0; --i) {
        if (userData[i]>=0) {
            var unitData = {
                id: userData[i],
                type: MONSTERS[userData[i]].type,
                atk: MONSTERS[userData[i]].atk,
                matk: MONSTERS[userData[i]].atk,
                hp: MONSTERS[userData[i]].hp,
                mhp: MONSTERS[userData[i]].hp,
                tatk: 0,
            }
            battleData.setup.push(unitData);
        } else if (userData[i]<-1) {
            var hpos = -2*1-userData[i];
            var promoValue = 0;
            if (prom[hpos]===5) promoValue = promoData[hpos].skill;
            var skillVal = HERO[hpos].skill.value + promoValue;
            var stats = level2stats(hpos,hero[hpos],prom[hpos],HERO[hpos].skill.type=="evo"?skillVal:((HERO[hpos].skill.type=="easter"&&easter)?skillVal:1));
            if (HERO[hpos].skill.type=="ratk") {
                var val = seed%((skillVal)+1);
                stats.atk+=val;
                stats.hp+=val;
            }
            if (HERO[hpos].skill.type=="posbonus") {
                var behind = 0;
                for (var j=i-1; j>=0; --j) if (userData[j]!=-1) ++behind;
                var val = skillVal*behind;
                stats.atk+=val;
                stats.hp+=val;
            }
			if (HERO[hpos].skill.type=="aposbonus") {
                var behind = 0;
                for (var j=i-1; j>=0; --j) if (userData[j]!=-1) ++behind;
                var val = Math.round(skillVal*hero[hpos]*behind);
                stats.atk+=val;
                stats.hp+=val;
            }
            var unitData = {
                id: userData[i],
                type: HERO[hpos].type,
                atk: stats.atk,
                matk: stats.atk,
                tatk: 0,
                hp: stats.hp,
                mhp: stats.hp,
                lvl: hero[hpos],
                prom: prom[hpos],
                skill: HERO[hpos].skill,
            }
            battleData.setup.push(unitData);
            if (HERO[hpos].rarity==5) {
                battleData.setup[battleData.setup.length-1].atk+=WBatk(battleData.setup[battleData.setup.length-1].lvl);
                gBattle.isBossBattle = true;
            }
        }
    }
    battleData.dmg=0;
    return seed;
}

